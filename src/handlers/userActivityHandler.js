const {Timestamp} = require('@google-cloud/firestore');
const {db} = require('../../firestore');
const axios = require('axios');

const getActivityRecomendation = async (request, h) => {
  try {
    const user = request.authUser;
    const {startTime, endTime} = request.query;

    const dst = new Date(startTime);
    const st = dst.getUTCHours() * 60 + dst.getMinutes();
    const det = new Date(endTime);
    const et = det.getUTCHours() * 60 + det.getMinutes();
    const dur = et - st;

    const activityRecomendationsRes = await axios.get(
      'http://34.173.91.244/activity?' +
        'age=' +
        user.age +
        '&lastEducation=' +
        user.lastEducation +
        '&job=' +
        user.job +
        '&gender=' +
        user.gender +
        '&education=' +
        user.education +
        '&employmentType=' +
        user.employmentType +
        '&startTime=' +
        st +
        '&duration=' +
        dur,
    );
    const activityRecomendations = activityRecomendationsRes.data;

    const translate = async (text) => {
      const config = {
        method: 'post',
        url: 'https://translation.googleapis.com/language/translate/v2',
        headers: {
          // eslint-disable-next-line quote-props
          Authorization: request.headers.authorization,
          'Content-Type': 'application/json',
        },
        data: {
          q: text[0].toUpperCase() + text.substr(1),
          source: 'en',
          target: 'id',
          format: 'text',
        },
      };

      return axios
        .request(config)
        .then((response) => {
          // console.log(response.data.data.translations[0].translatedText);
          return response.data.data.translations[0].translatedText;
        })
        .catch((error) => {
          console.log(error);
        });
    };

    const activities = [];
    for (const key in activityRecomendations) {
      activities.push(await translate(activityRecomendations[key]));
    }

    const response = h.response({
      data: activities,
    });
    response.code(200);
    return response;
  } catch (err) {
    const response = h.response({
      message: err.message,
    });
    response.code(500);
    return response;
  }
};

const createUserActivityHandler = async (request, h) => {
  try {
    const {activityName, description, startTime, endTime} = request.payload;
    const userId = request.authUser.email;

    // check request body payload
    if (!activityName || !startTime || !endTime) {
      const response = h.response({
        message: 'bad request',
      });
      response.code(400);
      return response;
    }

    const dst = new Date(startTime);
    const st = dst.getUTCHours() * 60 + dst.getMinutes();
    const det = new Date(endTime);
    const et = det.getUTCHours() * 60 + det.getMinutes();
    const dur = et - st;

    const user = request.authUser;
    const difficultyRes = await axios.get(
      'http://34.173.91.244/difficulty?' +
        'age=' +
        user.age +
        '&lastEducation=' +
        user.lastEducation +
        '&job=' +
        user.job +
        '&gender=' +
        user.gender +
        '&education=' +
        user.education +
        '&employmentType=' +
        user.employmentType +
        '&activityName=' +
        activityName +
        '&startTime=' +
        st +
        '&duration=' +
        dur,
    );
    const difficulty = difficultyRes.data;

    // create a new activities with autogenerated id
    const activitiesRes = await db.collection('userActivities').add({
      userId: userId,
      activityName: activityName,
      description: description || '',
      startTime: Timestamp.fromDate(new Date(startTime)),
      endTime: Timestamp.fromDate(new Date(endTime)),
      finish: false,
      difficulty: difficulty.difficulty,
      exp: difficulty.exp,
    });
    // update the activities id property
    db.collection('userActivities').doc(activitiesRes.id).update({
      activityId: activitiesRes.id,
    });

    const response = h.response({
      message: 'activity successfully created',
      data: {
        activityId: activitiesRes.id,
      },
    });
    response.code(201);
    return response;
  } catch (err) {
    const response = h.response({
      message: err.message,
    });
    response.code(500);
    return response;
  }
};

